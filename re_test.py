# -*- coding:utf-8 -*-
import re
kk = re.compile(r'\d+')
print(kk.findall('one1two2three3four4'))
# ['1', '2', '3', '4']
#===============================================================
#       注意此处findall()的用法，可传两个参数;
#===============================================================
kk = re.compile(r'\d')
print(re.findall(kk,"one123"))
# ['1', '2', '3']
#===============================================================
#    贪婪模式，最大匹配
#===============================================================
#    describe:  没括号
#===============================================================
string="2345  3456  4567  5678"
regex=re.compile("\w+\s+\w+")
print(regex.findall(string))
# ['2345  3456', '4567  5678']
#===============================================================
#    describe:  一个括号
#===============================================================
regex=re.compile("(\w+)\s+\w+")
print(regex.findall(string))
# ['2345', '4567']
#===============================================================
#    describe:  两个括号
#===============================================================
regex=re.compile("((\w+)\s+\w+)")
print(regex.findall(string))
# [('2345  3456', '2345'), ('4567  5678', '4567')]
